#include "libft.h"

void		ft_putnbr_fd(int n, int fd)
{
	long	i;
	int		d;

	i = n;
	d = 1;
	if (i < 0)
	{
		ft_putchar_fd('-', fd);
		i = -i;
	}
	while ((i / d) >= 10)
		d *= 10;
	while ((d != 0))
	{
		ft_putchar_fd((i / d) + '0', fd);
		i = i % d;
		d /= 10;
	}
}
