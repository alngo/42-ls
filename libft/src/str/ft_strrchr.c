#include "libft.h"

char				*ft_strrchr(const char *s, int c)
{
	unsigned char	ch;
	const char		*us;

	ch = c;
	while (1)
	{
		if (*s == ch)
			us = s;
		if (!(*s))
			return ((char *)us);
		s++;
	}
}
